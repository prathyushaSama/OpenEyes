<h1>Which Patients Will Go Onto OpenEyes?</h1>
<p>All new patients to the Cataract service will be recorded on OpenEyes
 and the full electronic record will be available by the patient's day
 of surgery.</p>
<p>&#160;</p>
<p>However, some patients will already have been through pre-assessment
 and will not have an existing record on OpenEyes when the come in for
 surgery. Ophthalmologists still need to capture details of these patients
 to meet the 100 operations target. This is so the surgeon can record a
 cut-down version of the patient's history in theatre when they are recording
 details of the operation itself.</p>