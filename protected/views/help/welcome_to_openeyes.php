<h1>Welcome To OpenEyes</h1>
<h2>What is OpenEyes?</h2>
<p>OpenEyes is a new specialist ophthalmic Electronic Medical Record
    (EMR) system:</p>
<ul>
    <li>to automate and simplify the way Ophthalmologists capture
        ophthalmic medical records. However, paper-based practices still
        need to be done while additional functionality is completed in
        OpenEyes.</li>
    <li>to comply with national cataract audit requirements set by the
        Royal College of Ophthalmologists. Ophthalmologists will be
        contributing to the cataract audit from the first patient
        recorded on OpenEyes. There is a target of at least 100
        operations recorded per cataract surgeon within OpenEyes so it
        is crucial that the system be used as soon as possible.</li>
</ul>