<h1>What OpenEyes Cannot Yet Do</h1>
<p>Currently, there are some areas that OpenEyes will not be able to
    handle electronically. The following need to stay on paper:</p>
<ul>
    <li>general observations (blood pressure, pulse, temperature and
        similar)</li>
    <li>Aside from INR, blood tests cannot be recorded in OpenEyes. The
        system allows you to add specific risks (e.g. vCJD, MRSA, cannot
        lie flat)</li>
    <li>full pre-assessment, pre-surgery checks and some other aspects
        because OpenEyes does not have a &quot;checklist&quot; function.</li>
</ul>