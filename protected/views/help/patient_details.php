<h1>How Are Patient Details Maintained in OpenEyes?</h1>
<p>Some patient details, for instance their name, age, address, GP and
    similar, can be maintained in PAS so that the core hospital systems
    are always correct. These details cannot be edited in OpenEyes and
    MUST be edited in PAS; the OpenEyes - PAS link will ensure that all
    changes made to these details in PAS are copied to OpenEyes within a
    few minutes.</p>